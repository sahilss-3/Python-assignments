# Create a list
numbers = [5, 2, 9, 1]

# Access elements
print(numbers[0])
print(numbers[2])

# Add elements
numbers.append(7)
print(numbers)

# Remove element
numbers.remove(2)
print(numbers)

# Sort list
numbers.sort()
print(numbers)

# Reverse list
numbers.reverse()
print(numbers)
